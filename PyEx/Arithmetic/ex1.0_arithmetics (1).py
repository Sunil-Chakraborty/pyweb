# addition
print(2 + 3)
# with formatting
print('2 + 3 = %d' % (2 + 3))

# subtraction
print(20.1 - 1)

# division
print('15/10 = ', 15 / 10)
print('15//10 = ', 15 // 10) # floor division, result is integer

# multiplication
print(99 * 100 * 2)

# remainder
print(10 % 8)
print(3 % 6)

# power
print(2**8)
print(2**16)
print(2**32)
print(2**0.5)
print(100**0.5)


